    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (tfNome.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Preencha o campo nome");
        }
        else
        {
            int pos = nomes.indexOf(tfNome.getText());
            if (pos != -1){
                taSaida.setText(nomes.get(pos) + "   |   Posição: " + pos + "  |  Idade: " + idades.get(pos) + "\n");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Nome não localizado");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

