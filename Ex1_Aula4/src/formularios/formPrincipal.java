    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (tfNome.getText().equals("") || tfIdade.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha os dois campos");
        }
        else
        {
            String nome = tfNome.getText();
            
            if (!nomes.contains(nome)){
                nomes.add(nome);
                idades.add(Integer.parseInt(tfIdade.getText()));
                tfIdade.setText("");
                tfNome.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Nome já existe!!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        taSaida.setText("");
        for (int i=0; i<nomes.size(); i++){
            taSaida.append(nomes.get(i) + "  |  Idade: " + idades.get(i) + "\n");
            tfNome.setText("");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (tfNome.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Preencha o campo nome");
        }
        else
        {
            int pos = nomes.indexOf(tfNome.getText());
            if (pos != -1){
                nomes.remove(pos);
                idades.remove(pos);
                tfNome.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Nome não localizado");
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String auxNome;
        int auxId;
        for (int i=0; i<nomes.size()-1; i++){
            for (int j=i+1; j<nomes.size(); j++){
                if (nomes.get(i).compareTo(nomes.get(j)) > 0){
                    auxNome = nomes.get(i);
                    auxId = idades.get(i);
                    nomes.set(i, nomes.get(j));
                    nomes.set(j, auxNome);
                    idades.set(i, idades.get(j));
                    idades.set(j, auxId);
                
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Lista ordenada");
    }//GEN-LAST:event_jButton2ActionPerformed

